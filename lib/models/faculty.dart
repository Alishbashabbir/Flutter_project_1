class Faculty {
  final String id;
  final String name;
  final String dean;
  final String professorName;
  final String professorDesignation;
  final String phoneNumber;
  final String email;
  final String address;
  final String subjects;

  Faculty({
    required this.id,
    required this.name,
    required this.dean,
    required this.professorName,
    required this.professorDesignation,
    required this.phoneNumber,
    required this.email,
    required this.address,
    required this.subjects,
  });
}
